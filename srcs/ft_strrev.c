#include "libft.h"

char	*ft_strrev(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
		++i;
	--i;
	while (i > j)
	{
		ft_cswap(&str[i], &str[j]);
		--i;
		++j;
	}
	return (str);
}
